int nth_prime( int n )
{
	
	unsigned int nthPrime{2} ;
	unsigned int PrimeNums{0} ;
	bool flag {false} ; 
	
	int Num {2} ;
	
	while( PrimeNums < n )
	{
		
		flag = is_prime( Num ) ;
		
		if( flag == true )
		{
			PrimeNums++ ;
		}
		
		if( PrimeNums == n )
		{
			nthPrime = Num ;
		}
		
		Num++ ;
	}
	
	
	return nthPrime ;
	
}

