bool is_prime( int num ) 
{
	
	bool isPrime {false} ;
	
	if( (num == 2) || (num == 3) )
	{
		isPrime = true ;
	}
	else if( (num % 2 != 0) && (num % 3 != 0) )
	{
		isPrime = true ;
	}
	else
	{
		isPrime = false ;
	}
	
	return isPrime ;
	
}

