string reverse_str( const string &str ) 
{
	
	int strLen = str.length( ) - 1 ;

	string revStr = "" ;
	
	for( int i = strLen; str[i] > 0; i-- )
	{
		revStr += str[i] ;
	}

	return revStr ;
	
}

